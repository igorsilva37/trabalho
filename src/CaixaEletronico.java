    private void C1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_C1ActionPerformed
        // TODO add your handling code here:
        Iterator<Dados> bfb = listaSenha.iterator();
        
        while(bfb.hasNext()){
            Dados snh = bfb.next();
            bfb.remove();
            painel.setText(String.valueOf(snh.getPrioritario())+" - "+String.valueOf(snh.getNumP()));
            break;
            
        }
        
        
    }//GEN-LAST:event_C1ActionPerformed

